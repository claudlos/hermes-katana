"""
KatanaInstaller — manages Katana integration into Hermes checkouts.

Combines camelup's approach (lightweight shim installation) with aegis's
patch system (source-level injection).  The installer handles:

- Detection of Hermes checkouts
- Source patch application / reversion
- Configuration file generation
- CA certificate generation for the MITM proxy
- Installation verification and status reporting

Usage::

    installer = KatanaInstaller()

    if installer.detect_hermes("/path/to/hermes"):
        installer.install("/path/to/hermes")
        result = installer.verify("/path/to/hermes")
        print(result.summary)
"""

